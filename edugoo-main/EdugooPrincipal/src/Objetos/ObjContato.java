package Objetos;

public class ObjContato {
    private int contatoId;
    private String contatoNomeUser;
    private String contatoEmailUser;
    private String contatoPergunta;
    private int contatoUsuarioId;

    public int getContatoId() {
        return contatoId;
    }

    public void setContatoId(int contatoId) {
        this.contatoId = contatoId;
    }

    public String getContatoNomeUser() {
        return contatoNomeUser;
    }

    public void setContatoNomeUser(String contatoNomeUser) {
        this.contatoNomeUser = contatoNomeUser;
    }

    public String getContatoEmailUser() {
        return contatoEmailUser;
    }

    public void setContatoEmailUser(String contatoEmailUser) {
        this.contatoEmailUser = contatoEmailUser;
    }

    public String getContatoPergunta() {
        return contatoPergunta;
    }

    public void setContatoPergunta(String contatoPergunta) {
        this.contatoPergunta = contatoPergunta;
    }

    public int getContatoUsuarioId() {
        return contatoUsuarioId;
    }

    public void setContatoUsuarioId(int contatoUsuarioId) {
        this.contatoUsuarioId = contatoUsuarioId;
    }
    
    
}
