package Objetos;

public class ObjVaga {
    private int vagaId;
    private String vagaTitulo;
    private String vagaAreaAtuacao;
    private String vagaInfo;
    private int vagaUsuarioId;

    public int getVagaId() {
        return vagaId;
    }

    public void setVagaId(int vagaId) {
        this.vagaId = vagaId;
    }

    public String getVagaTitulo() {
        return vagaTitulo;
    }

    public void setVagaTitulo(String vagaTitulo) {
        this.vagaTitulo = vagaTitulo;
    }

    public String getVagaAreaAtuacao() {
        return vagaAreaAtuacao;
    }

    public void setVagaAreaAtuacao(String vagaAreaAtuacao) {
        this.vagaAreaAtuacao = vagaAreaAtuacao;
    }

    public String getVagaInfo() {
        return vagaInfo;
    }

    public void setVagaInfo(String vagaInfo) {
        this.vagaInfo = vagaInfo;
    }

    public int getVagaUsuarioId() {
        return vagaUsuarioId;
    }

    public void setVagaUsuarioId(int vagaUsuarioId) {
        this.vagaUsuarioId = vagaUsuarioId;
    }
}
